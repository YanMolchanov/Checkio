def best_stock(stocks):
    result = ''
    max_stock = 0
    for s in stocks:
        if max_stock < stocks.get(s):
            max_stock = stocks.get(s)
            result = s
    return result


if __name__ == '__main__':
    print("Example:")
    print(best_stock({
        'CAC': 10.0,
        'ATX': 390.2,
        'WIG': 1.2
    }))

